func factorial(n:Int) -> Int{
    var fact : Int
    print("Calculating factorial(\(n))...")
    if n == 1{
        print("Terminal case, returning 1.")
        fact = 1
    }else{
        fact = n * factorial(n:n-1)
        print("Non-terminal case, returning \(fact).")
    }
    return fact
}

let x = factorial(n:n)
print("The final result is: \(x).")


